package com.uncreative.game;

public class WeatherObstacle extends Obstacle {
    private Integer xpMultiplier;

    public WeatherObstacle(Integer damage, String type, Location location, Integer mult){
        super(damage, type, location);
        this.xpMultiplier = mult;
    }

    public Integer getMultiplier(){
        return this.xpMultiplier;
    }
}